def loadLotteries(url: str):
    f1 = pd.read_excel(url, sheet_name='gain')
    f2 = pd.read_excel(url, sheet_name='loss')
    d = {}

    if f1.columns.tolist() != f2.columns.tolist():
        return None
    else:
        cols = f1.columns.tolist()
        for col in cols:
            d[col] = []
            for f1row in np.arange(0, len(f1), 1):
                d[col].append(f1[col].loc[f1row])
            for f2row in np.arange(0, len(f2), 1):
                d[col].append(f2[col].loc[f2row])
        return d


data = loadLotteries('_static/data/lotteries.xls')


def separateLotteries(d: dict) -> list[dict]:
    d1 = dict(d)
    d2 = dict(d)
    d1['asc'] = []
    d2['asc'] = []
    for i in np.arange(0, len(d[list(d.keys())[0]]), 1):
        randomizer = rd.choice([True, False])
        if randomizer is True:
            d1['asc'].append(randomizer)
            d2['asc'].append(not randomizer)
        if randomizer is False:
            d1['asc'].append(randomizer)
            d2['asc'].append(not randomizer)
    return [d1, d2]


data = separateLotteries(data)


def getOptionA(block: int, table: int) -> dict:
    return {
        'p1': str("{0:.0%}".format(data[block]['p'][table])),
        'p2': str("{0:.0%}".format(1-data[block]['p'][table])),
        'x1': str("{:.2f}".format(data[block]['x1'][table])) + str(' Punkte'),
        'x2': str("{:.2f}".format(data[block]['x2'][table])) + str(' Punkte')
    }


def getOptionB(block: int, table: int) -> list:
    li = []
    b_start = data[block]['x1'][table]
    b_step = (-1)*(data[block]['x1'][table] - data[block]['x2'][table])/20
    b_stop = data[block]['x2'][table] + b_step
    for i in np.arange(b_start, b_stop, b_step):
        li.append(str("{:.2f}".format(i)) + str(' Punkte'))
    if data[block]['asc'][table] is True:
        li.reverse()
    return li


def getTable(block: int, table: int) -> dict:
    return {
        'Number': table,
        'Numbering': np.arange(1, 21 + 1).tolist(),
        'A': getOptionA(block-1, table-1),
        'B': getOptionB(block-1, table-1),
        'ASC': data[block-1]['asc'][table-1],
        'Choice': [
            [1, 'A'],
            [2, 'B']
        ],
        'Widget': widgets.RadioSelectHorizontal
    }


